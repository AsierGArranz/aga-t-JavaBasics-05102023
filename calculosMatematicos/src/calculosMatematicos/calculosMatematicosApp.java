package calculosMatematicos;

public class calculosMatematicosApp {

	public static void main(String[] args) {
		
		System.out.println("Ejercicio 1: ");

		int a= 5;
		int b = 10;
		int suma = a+b;
		int resta = a-b;
		int multi = a*b;
		double divi = a%b;
		
		System.out.println("Suma de 5 + 10 = "+suma);
		System.out.println("Resta de 5 - 10 = "+resta);
		System.out.println("Multiplicacion de 5 * 10 = "+multi);
		System.out.println("Division de 5 / 10 = "+divi);

	}

}
